/*
题目：请输入一个8位的十进制整数，编写程序取出该整数的中间4位数，分别输出取出的这4位数以及该4位数加上1024的得数。
输入：一个整数。
输出：两个整数，用空格分隔。
*/

#include <stdio.h>
int main(void)
{
	int x,y,z,a,b,c,d;			//输入、输出结果分别为x,y	中间变量z用于取前6位数	中间四位数为abcd
	scanf_s("%d", &x);

	z = x % 1000000;
	a = z / 100000 % 10;
	b = z / 10000 % 10;
	c = z / 1000 % 10;
	d = z / 100 % 10;

	y = a * 1000 + b * 100 + c * 10 + d;
	printf("%d %d", y, y + 1024);
	return 0;
}